#include"libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	int	i;

	if (!(unsigned char *)src && !(unsigned char *)dst)
		return (0);
	if (src < dst)
	{
		i = (int)len -1;
		while (i >= 0)
		{
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
			i--;
		}
	}
	else
	{
		i = 0;
		while (i < (int)len)
		{
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
			i++;
		}
	}
	return (dst);
}
